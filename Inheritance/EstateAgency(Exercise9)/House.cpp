#include "stdafx.h"
#include "House.h"
#include <iostream>

using namespace std;

//-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
House::House():Estate()
{
	numberOfRooms = 0;
	numberOfFloors = 0;
	yardArea = 0;
}
//-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
House::House(int pnumberOfRooms, int pnumberOfFloors, float pyardArea, char *paddress, char *powner, double pprice, float parea):Estate(paddress, powner, pprice, parea)
{
	numberOfRooms = pnumberOfRooms;
	numberOfFloors = pnumberOfFloors;
	yardArea = pyardArea;
}
//-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
House::~House()
{
}
//-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
void House::Print()
{
	cout << address << "\t" << owner << "\t" << price << "\t" << area << "\t" << numberOfRooms << "\t" << numberOfFloors << "\t" << yardArea << "\t" << endl;
}
