#include <iostream>

using namespace std;

//-----------------------------------------------------------------------------------------------------------------------------------------------------------
Apartament::Apartament():Estate()
{
	numberOfRooms = 0;
	floor = 0;
}
//-----------------------------------------------------------------------------------------------------------------------------------------------------------
Apartament::Apartament(int pnumberOfRooms, int pfloor, char *paddress, char *powner, double pprice, float parea):Estate(paddress, powner, pprice, parea)
{
	numberOfRooms = pnumberOfRooms;
	floor = pfloor;
}
//-----------------------------------------------------------------------------------------------------------------------------------------------------------
Apartament::~Apartament()
{
	
}
//-----------------------------------------------------------------------------------------------------------------------------------------------------------
void Apartament::Print()
{
	cout << address << "\t" << owner << "\t" << price << "\t" << area << "\t"  << numberOfRooms << "\t" << floor << endl;
}
